const titleFromPublicId = (publicId, index) => {
  const cleaned = publicId
    .replace(/_[a-z0-9]{6}$/i, "")
    .replace(/^微信图片_?/, "Frame ")
    .replace(/^img/i, "Frame ")
    .replace(/_/g, " ")
    .trim();

  return cleaned || `Frame ${String(index + 1).padStart(2, "0")}`;
};

const filenameFromUrl = (url) => {
  const encodedFilename = url.split("/").pop()?.replace(/\.[^.]+$/, "") ?? "";

  try {
    return decodeURIComponent(encodedFilename);
  } catch {
    return encodedFilename;
  }
};

const displayNameFromCloudinaryUrl = (url, index) => {
  const filename = filenameFromUrl(url);
  const cleaned = filename
    .replace(/_[a-z0-9]{6}$/i, "")
    .replace(/^微信图片_?/, "")
    .replace(/^img/i, "Frame ")
    .replace(/_/g, " ")
    .trim();

  return cleaned || titleFromPublicId(filename, index);
};

